# Other str functions

# 1. len()
# Usage: Returns the length of the string.
# Example: len("Hello") returns 5.
# 2. str.upper()
# Usage: Converts all characters in the string to uppercase.
# Example: "hello".upper() returns "HELLO".
# 3. str.lower()
# Usage: Converts all characters in the string to lowercase.
# Example: "HELLO".lower() returns "hello".
# 4. str.capitalize()
# Usage: Capitalizes the first character of the string.
# Example: "hello".capitalize() returns "Hello".
# 5. str.title()
# Usage: Capitalizes the first character of each word in the string.
# Example: "hello world".title() returns "Hello World".
# 6. str.strip()
# Usage: Removes any leading and trailing whitespace from the string.
# Example: " hello ".strip() returns "hello".
# 7. str.replace(old, new)
# Usage: Replaces occurrences of a substring with another substring.
# Example: "hello world".replace("world", "Python") returns "hello Python".
# 8. str.find(sub)
# Usage: Returns the lowest index where the substring is found, or -1 if not found.
# Example: "hello".find("e") returns 1.
# 9. str.split(separator)
# Usage: Splits the string into a list of substrings based on the specified separator.
# Example: "hello world".split(" ") returns ["hello", "world"].
# 10. str.join(iterable)
# Usage: Joins elements of an iterable (e.g., list) into a single string, separated by the string on which it's called.
# Example: ", ".join(["apple", "banana", "cherry"]) returns "apple, banana, cherry".
# 11. str.startswith(prefix)
# Usage: Returns True if the string starts with the specified prefix, otherwise False.
# Example: "hello".startswith("he") returns True.
# 12. str.endswith(suffix)
# Usage: Returns True if the string ends with the specified suffix, otherwise False.
# Example: "hello".endswith("lo") returns True.
# 13. str.isdigit()
# Usage: Returns True if all characters in the string are digits, otherwise False.
# Example: "123".isdigit() returns True.
# 14. str.isalpha()
# Usage: Returns True if all characters in the string are alphabetic, otherwise False.
# Example: "hello".isalpha() returns True.
# 15. str.isalnum()
# Usage: Returns True if all characters in the string are alphanumeric, otherwise False.
# Example: "hello123".isalnum() returns True.





